namespace UnityEngine.Localization.SmartFormat.Core.Settings
{
    /// <summary>
    /// Options for the type of comparison to perform.
    /// </summary>
    public enum CaseSensitivityType
    {
        /// <summary>
        /// Uppercase and lowercase letters are treated as distinct.
        /// </summary>
        CaseSensitive,

        /// <summary>
        /// Uppercase and lowercase letters are treated as the same.
        /// </summary>
        CaseInsensitive
    }
}
